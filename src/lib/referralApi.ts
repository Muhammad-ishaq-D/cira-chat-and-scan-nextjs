/**
 * Referral Letter API Client
 * Provides typed wrappers for POST /api/referral/generate and POST /api/referral/pdf
 */

import { getToken } from "./auth";

const API_BASE = import.meta.env.VITE_API_URL || "https://askainurse.com";

// ─── Types ───────────────────────────────────────────────────────────────────

